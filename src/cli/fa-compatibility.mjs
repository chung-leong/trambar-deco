function translateClass4To5(className) {
  if (!className) {
    return;
  }
  let entry;
  if (className.startsWith('fa-')) {
    entry = map[className.substr(3)];
  }
  if (entry) {
    if (entry instanceof Array) {
      const [ newName, prefix ] = entry;
      return `${prefix} fa-${newName}`;
    } else {
      const prefix = entry;
      return `${prefix} ${className}`;
    }
  } else {
    return `fas ${className}`;
  }
}

